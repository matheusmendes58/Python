n1 = float(input('Digite o primeiro valor: '))
n2 = float(input('Digite o segundo valor: '))
s = n1 + n2  # soma
m = n1 * n2  # mutiplicação
d = n1 / n2  # divisão
di = n1 // n2  # divisão inteira
e = n1 ** n2  # potenciação
r = n1 % n2  # divisão mas resultado só mostrara o resto
print('a soma é {} a multiplicação é {} a divisão é {} a divisção inteira é {}'.format(s, m, d, di))
print('a potencia é {} e o resto da divisão é {}'.format(e, r))




